# 9.3 Creating Procedures using MySQL Workbench

-- By using the Workbench interface, we do not need to set a new DELIMITER and then revert back to the normal DELIMITER after.

-- Creaging procedures using the MySQL Workbench interface:
    -- Right Click the 'Stored Procedures' dropdown menu (see area on the left)
    -- Click on 'Create Stored Procedure'
    -- Write the code needed for the procedure
    -- Once done, click on 'Apply' or 'Revert'